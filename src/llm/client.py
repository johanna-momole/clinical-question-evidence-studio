# ── Abstract base ──────────────────────────────────────────────────────────────


class BaseLLMClient(ABC):
    """Abstract base for all LLM provider clients."""

    @abstractmethod
    def complete(
        self,
        messages: list[LLMMessage],
        task: str = "default",
        max_tokens: int = 1024,
        **kwargs: Any,
    ) -> LLMResponse:
        """Send messages and return a structured response."""
        ...

    @staticmethod
    def _hash_prompt(messages: list[LLMMessage]) -> str:
        text = "||".join(f"{m.role}:{m.content}" for m in messages)
        return hashlib.sha256(text.encode()).hexdigest()[:16]


# ── Demo client ────────────────────────────────────────────────────────────────


class DemoLLMClient(BaseLLMClient):
    """Deterministic demo client that returns pre-computed fixture responses.

    Always available — used when no API key is configured or when DEMO_MODE=true.
    Responses are clearly labeled '[DEMO MODE]' so users are never misled.
    """

    def complete(
        self,
        messages: list[LLMMessage],
        task: str = "default",
        max_tokens: int = 1024,
        **kwargs: Any,
    ) -> LLMResponse:
        content = _DEMO_RESPONSES.get(task, _DEMO_RESPONSES["default"])
        logger.debug("DemoLLMClient: serving pre-computed response for task='%s'", task)
        return LLMResponse(
            content=content,
            model="demo",
            is_demo_mode=True,
            prompt_hash=self._hash_prompt(messages),
            task=task,
        )


# ── Anthropic client ──────────────────────────────────────────────────────────


class AnthropicLLMClient(BaseLLMClient):
    """LLM client backed by the Anthropic Messages API."""

    def __init__(self, api_key: str, model: str = "claude-sonnet-4-6") -> None:
        if not _ANTHROPIC_AVAILABLE:
            raise ImportError(
                "anthropic package not installed. Run: pip install 'anthropic>=0.25.0'"
            )
        self._client = _anthropic.Anthropic(api_key=api_key)
        self.model = model

    def complete(
        self,
        messages: list[LLMMessage],
        task: str = "default",
        max_tokens: int = 1024,
        **kwargs: Any,
    ) -> LLMResponse:
        api_messages = [
            {"role": m.role, "content": m.content} for m in messages if m.role != "system"
        ]
        system = next((m.content for m in messages if m.role == "system"), None)

        response = self._client.messages.create(
            model=self.model,
            max_tokens=max_tokens,
            messages=api_messages,
            **({"system": system} if system else {}),
        )
        content = response.content[0].text if response.content else ""
        tokens = (response.usage.input_tokens or 0) + (response.usage.output_tokens or 0)

        return LLMResponse(
            content=content,
            model=self.model,
            is_demo_mode=False,
            tokens_used=tokens,
            prompt_hash=self._hash_prompt(messages),
            task=task,
        )


# ── OpenAI client ──────────────────────────────────────────────────────────────


class OpenAILLMClient(BaseLLMClient):
    """LLM client backed by the OpenAI Chat Completions API."""

    def __init__(self, api_key: str, model: str = "gpt-4o") -> None:
        if not _OPENAI_AVAILABLE:
            raise ImportError("openai package not installed. Run: pip install 'openai>=1.14.0'")
        self._client = _openai.OpenAI(api_key=api_key)
        self.model = model

    def complete(
        self,
        messages: list[LLMMessage],
        task: str = "default",
        max_tokens: int = 1024,
        **kwargs: Any,
    ) -> LLMResponse:
        api_messages = [{"role": m.role, "content": m.content} for m in messages]

        response = self._client.chat.completions.create(
            model=self.model,
            messages=api_messages,
            max_tokens=max_tokens,
        )
        content = response.choices[0].message.content or ""
        tokens = response.usage.total_tokens if response.usage else None

        return LLMResponse(
            content=content,
            model=self.model,
            is_demo_mode=False,
            tokens_used=tokens,
            prompt_hash=self._hash_prompt(messages),
            task=task,
        )


# ── Factory ────────────────────────────────────────────────────────────────────


def get_llm_client(
    provider: str | None = None,
    api_key: str | None = None,
    model: str | None = None,
) -> BaseLLMClient:
    """Return the appropriate LLM client based on configuration.

    Falls back to DemoLLMClient when no provider is configured or packages
    are unavailable. This ensures the application always works offline.
    """
    from src.config.settings import get_settings

    settings = get_settings()
    resolved_provider = provider or settings.llm_provider
    resolved_model = model or settings.active_llm_model

    if settings.is_demo_mode or not resolved_provider:
        logger.info("LLM client: using DemoLLMClient (demo mode)")
        return DemoLLMClient()

    if resolved_provider == "anthropic":
        key = api_key or settings.anthropic_api_key
        if not key:
            logger.warning(
                "Anthropic provider configured but no API key found; falling back to demo"
            )
            return DemoLLMClient()
        return AnthropicLLMClient(api_key=key, model=resolved_model)

    if resolved_provider == "openai":
        key = api_key or settings.openai_api_key
        if not key:
            logger.warning("OpenAI provider configured but no API key found; falling back to demo")
            return DemoLLMClient()
        return OpenAILLMClient(api_key=key, model=resolved_model)

    logger.warning("Unknown LLM provider '%s'; falling back to demo", resolved_provider)
    return DemoLLMClient()
