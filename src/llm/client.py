"""Provider-agnostic LLM client interface.

Supports Anthropic (claude-*), OpenAI (gpt-*), and a deterministic demo mode
that returns pre-computed fixture responses so the application runs without
any API key.

Usage:
    client = get_llm_client()          # auto-selects based on settings
    response = client.complete(messages, task="pico_parse")
"""

import hashlib
import logging
from abc import ABC, abstractmethod
from typing import Any

from pydantic import BaseModel

logger = logging.getLogger(__name__)

# Optional provider imports — application does not fail if packages are absent
try:
    import anthropic as _anthropic

    _ANTHROPIC_AVAILABLE = True
except ImportError:
    _ANTHROPIC_AVAILABLE = False

try:
    import openai as _openai

    _OPENAI_AVAILABLE = True
except ImportError:
    _OPENAI_AVAILABLE = False


class LLMMessage(BaseModel):
    """A single message in an LLM conversation."""

    role: str  # "user" | "assistant" | "system"
    content: str


class LLMResponse(BaseModel):
    """Structured response from any LLM provider or demo mode."""

    content: str = ""
    model: str = "demo"
    is_demo_mode: bool = True
    tokens_used: int | None = None
    prompt_hash: str | None = None
    task: str = "default"


